 Future<void> sendOTP(String email) async {
  EmailAuth emailAuth = EmailAuth(sessionName: 'Test Session');
  var res = await emailAuth.sendOtp(recipientMail: email, otpLength: 6);
  if (res) {
    print("OTP sent");
  } else {
    print("OTP not sent");
  }
}

